class Product {
  final String name;
  final String image;
  final double price;
  final double rating;
  final String description;
  final String category;
  final List<int> sizes;

  Product({
    required this.name,
    required this.image,
    required this.price,
    required this.rating,
    required this.description,
    required this.category,
    required this.sizes,
  });
}
